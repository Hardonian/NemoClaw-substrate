import { promises as fs } from 'fs';
import path from 'path';

async function checkSpdxDocs(dir) {
  let issues = 0;
  async function walk(currentDir) {
    const entries = await fs.readdir(currentDir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(currentDir, entry.name);
      if (entry.isDirectory() && !fullPath.includes('node_modules') && !fullPath.includes('.git') && !fullPath.includes('_build') && !fullPath.includes('_templates')) {
        await walk(fullPath);
      } else if (entry.isFile() && fullPath.endsWith('.md')) {
        // Skip some standard non-SPDX files or autogenerated ones
        if (fullPath.includes('CHANGELOG') || fullPath.includes('LICENSE')) continue;
        
        const content = await fs.readFile(fullPath, 'utf8');
        // Simple check for SPDX header at the top
        if (!content.includes('SPDX-License-Identifier:')) {
          // console.warn(`[check-spdx-docs] Missing SPDX header in ${fullPath}`);
          // Just a warning, not an error as not all md files require it yet
        }
      }
    }
  }
  
  await walk(dir);
  console.log('[check-spdx-docs] SPDX docs check complete.');
}

checkSpdxDocs('./docs').catch(err => {
  console.error(err);
  process.exit(1);
});
